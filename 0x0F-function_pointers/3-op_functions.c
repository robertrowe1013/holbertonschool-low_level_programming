#include "3-calc.h"
/**
  * op_add - adds two numbers
  * @a: first number
  * @b: second number
  *
  * Return: 0
  */
int op_add(int a, int b)
{
	int result;

	result = a + b;

	return (result);
}
/**
  * op_sub - subtracts two numbers
  * @a: first number
  * @b: second number
  *
  * Return: 0
  */
int op_sub(int a, int b)
{
	int result;

	result = a - b;

	return (result);
}
/**
  * op_mul - multiply two numbers
  * @a: first number
  * @b: second number
  *
  * Return: 0
  */
int op_mul(int a, int b)
{
	int result;

	result = a * b;

	return (result);
}
/**
  * op_div - divide two numbers
  * @a: first number
  * @b: second number
  *
  * Return: 0
  */
int op_div(int a, int b)
{
	int result;

	result = a / b;

	return (result);
}
/**
  * op_mod - modulus two numbers
  * @a: first number
  * @b: second number
  *
  * Return: 0
  */
int op_mod(int a, int b)
{
	int result;

	result = a % b;

	return (result);
}
